package javapractice.wrapperclasses;

public class WrapperClassesDemo {

    public static void main(String[] args) {

        byte a = 10;
        Byte b = a;

        System.out.println(b);

        Integer c = (int)a;
        System.out.println(c);

        Double d = 2.5;
        double x = d;
        System.out.println(x);

    }

}
